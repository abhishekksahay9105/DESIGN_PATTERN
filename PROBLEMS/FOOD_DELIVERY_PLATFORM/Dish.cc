Dish::Dish (std::string aDishName, int aPrice, std::string aImage, bool aIsNonVeg) : 
	mDishName(aDishName), 
	mPrice(aPrice), 
	mImage(aImage),
	mIsNonVeg (aIsNonVeg)
{
}


bool Dish::Decrement ()
{
	if (mCount == 0)
		return false;
	else
		mCount--;
	
	return true;
}


